package Operation;

/*
 * ≤‚ ‘ ++i ∫Õ  i++
 * */

public class AutoInc {
	public static void main(String[] args) {
		int i = 1, j = 1;
		System.out.println("i = " + i + " j = " + j);
		System.out.println("i++ = " + i++ + " ++j = " + ++j);
		System.out.println("i = " + i + " j = " + j);
	}
}
